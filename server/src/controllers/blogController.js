/**
 * Update blog
 */
export const updateBlog = asyncHandler(async (req, res) => {
  const { id } = req.params;
  const userId = req.userId;
  const { title, content, category } = req.body;

  const blog = await Blog.findById(id);

  if (!blog) {
    return res.status(404).json({ message: 'Blog not found' });
  }

  // Check if user is author
  if (blog.author.toString() !== userId) {
    return res.status(403).json({ message: 'Not authorized to update this blog' });
  }

  // Update fields
  if (title) blog.title = title;
  if (content) blog.content = content;
  if (category) blog.category = category;

  // Update image if new one was uploaded and processed
  if (req.imageUrl) {
    blog.imageUrl = req.imageUrl;
  }

  await blog.save();
  cache.clear();

  res.status(200).json({
    message: 'Blog updated successfully',
    blog,
  });
});

/**
 * Delete blog
 */
export const deleteBlog = asyncHandler(async (req, res) => {
  const { id } = req.params;
  const userId = req.userId;

  const blog = await Blog.findById(id);

  if (!blog) {
    return res.status(404).json({ message: 'Blog not found' });
  }

  // Check if user is author
  if (blog.author.toString() !== userId) {
    return res.status(403).json({ message: 'Not authorized to delete this blog' });
  }

  await Blog.findByIdAndDelete(id);
  cache.clear();

  res.status(200).json({ message: 'Blog deleted successfully' });
});

/**
 * Get trending blogs (auto-generated)
 */
export const getTrendingBlogs = asyncHandler(async (req, res) => {
  const limit = parseInt(req.query.limit) || 6;

  const blogs = await Blog.find({ autoGenerated: true })
    .sort({ views: -1, createdAt: -1 })
    .limit(limit)
    .populate('author', 'name email');

  res.status(200).json(blogs);
});

/**
 * Admin: Manually trigger trend blog generation
 */
export const triggerAutoBlogGeneration = asyncHandler(async (req, res) => {
  const user = await User.findById(req.userId).select('email name');

  if (!user) {
    return res.status(404).json({ message: 'User not found' });
  }

  const adminEmails = getAdminEmails();
  const isAdmin = adminEmails.includes(user.email?.toLowerCase());

  if (!isAdmin) {
    return res.status(403).json({
      message: 'Only admins can trigger auto-blog generation',
    });
  }

  await runAutoBlogGeneration(`manual API trigger by ${user.email}`);

  res.status(200).json({
    message: 'Auto-blog generation run completed',
  });
});
