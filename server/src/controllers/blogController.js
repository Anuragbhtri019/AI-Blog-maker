const getAdminEmails = () => {
  const configured = process.env.ADMIN_EMAILS;
  const defaults = ['system@vibeblog.ai'];

  if (!configured) return defaults;

  return configured
    .split(',')
    .map(email => email.trim().toLowerCase())
    .filter(Boolean);
};

/**
 * Create new blog
 */
export const createBlog = asyncHandler(async (req, res) => {
  const { title, content, category } = req.body;
  const userId = req.userId;

  if (!title || !content) {
    return res.status(400).json({ message: 'Please provide title and content' });
  }

  const blog = new Blog({
    title,
    content,
    category: category || 'General',
    author: userId,
    autoGenerated: false,
  });

  // Add image if provided
  if (req.imageUrl) {
    blog.imageUrl = req.imageUrl;
  }

  await blog.save();
  cache.clear();

  res.status(201).json({
    message: 'Blog created successfully',
    blog,
  });
});

/**
 * Get all blogs with pagination and filtering
 * Age-based visibility:
 * - < 1 week: visible to everyone
 * - 1 week - 1 month: visible only to logged-in users
 * - > 1 month: automatically deleted (shouldn't be queried)
 */
export const getAllBlogs = asyncHandler(async (req, res) => {
  const { page = 0, limit = 9, category } = req.query;
  const skip = parseInt(page) * parseInt(limit);

  const filter = {};

  // Category filtering
  if (category && category !== 'All') {
    filter.category = category;
  }

  // Age-based visibility filtering
  const now = new Date();
  const oneWeekAgo = new Date(now.getTime() - 7 * 24 * 60 * 60 * 1000);

  if (!req.userId) {
    // Unauthenticated: Only show blogs from the last 7 days
    filter.createdAt = { $gte: oneWeekAgo };
  } else {
    // Authenticated: Show all blogs (they're older than 1 month are deleted by cron)
    // No additional date filter needed
  }

  const blogs = await Blog.find(filter)
    .sort({ createdAt: -1 })
    .skip(skip)
    .limit(parseInt(limit))
    .populate('author', 'name email');

  const total = await Blog.countDocuments(filter);

  res.status(200).json({
    blogs,
    total,
    page: parseInt(page),
    pages: Math.ceil(total / parseInt(limit)),
  });
});

/**
 * Get single blog by ID or slug
 */
export const getBlogById = asyncHandler(async (req, res) => {
  const { id } = req.params;

  // Build query to search by slug or ID
  const query = {
    $or: [
      { slug: id } // Try slug first
    ]
  };

  // Only try ObjectId if id looks like a valid MongoDB ID
  if (/^[0-9a-fA-F]{24}$/.test(id)) {
    query.$or.push({ _id: id });
  }

  // Try to find by slug first (more user-friendly), then by ID
  let blog = await Blog.findOne(query).populate('author', 'name email');

  if (!blog) {
    return res.status(404).json({ message: 'Blog not found' });
  }

  // Increment views
  blog = await Blog.findByIdAndUpdate(
    blog._id,
    { $inc: { views: 1 } },
    { new: true }
  ).populate('author', 'name email');

  res.status(200).json(blog);
});

